
a=float(input("Upisi prvi broj: "))
op=input("Upisi operatera: ")
b=float(input("Upisi drugi broj: "))

if op == "+":
    print(a+b)
elif op == "-":
    print(a-b)
elif op == "*":
    print(a*b)
elif op == "/":
    print(a/b)
else:
    print("Kalkulator ovo ne moze izracunati")
    
